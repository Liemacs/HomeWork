package com.step.Calculator;

public class Calculator {
    public int sum(int a, int b) {
        return a+b;
    }
    public int difference(int a, int b) {
        return a-b;
    }
    public int multiplying(int a, int b) {
        return a*b;
    }
    public double division(int a, int b) {
        return (double)a/b;
    }

}
